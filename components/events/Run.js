    getRunScore(e) {
        let i;
        let runScore = this.state.runScore;

        i = runScore.scoreSheet[e];

        if (e != '') {
            if (this.props.mosLevel === '1') {
                if (e >= '18:00') {
                    return <Text style={styles.failed}>FAIL</Text>;
                } else if (e <= '12:45') {
                    return 100;
                } else {
                    return i;
                }
            } else if (this.props.mosLevel === '2') {
                if (e >= '19:00') {
                    return <Text style={styles.failed}>FAIL</Text>;
                } else if (e <= '12:45') {
                    return 100;
                } else {
                    return i;
                }
            } else if (this.props.mosLevel === '3') {
                if (e >= '21:07') {
                    return <Text style={styles.failed}>FAIL</Text>;
                } else if (e <= '12:45') {
                    return 100;
                } else {
                    return i;
                }
            }
        }
    }

